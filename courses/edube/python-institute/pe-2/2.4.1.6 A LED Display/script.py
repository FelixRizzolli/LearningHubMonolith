
def display(digits: int):
    leds = [
        ['###', '  #', '###', '###', '# #', '###', '###', '###', '###', '###'],
        ['# #', '  #', '  #', '  #', '# #', '#  ', '#  ', '  #', '# #', '# #'],
        ['# #', '  #', '###', '###', '###', '###', '###', '  #', '###', '###'],
        ['# #', '  #', '#  ', '  #', '  #', '  #', '# #', '  #', '# #', '  #'],
        ['###', '  #', '###', '###', '  #', '###', '###', '  #', '###', '###'],
    ]

    print_leds: list[str] = ['', '', '', '', '']

    for digit in str(digits):
        for i in range(0, 5, 1):
            print_leds[i] += (leds[i][int(digit)]) + ' '

    for i in range(0, 5, 1):
        print(print_leds[i])


display(123)
